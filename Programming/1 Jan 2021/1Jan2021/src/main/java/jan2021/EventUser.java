    private void jButton33ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton33ActionPerformed
        EventRegisterFrame.setVisible(false);
        RegisteredEventsFrame.setVisible(true);
        String event = jTextField11.getText();
        String[] regEvent = EventManagement.searchEventName(event);
        jTextField11.setText("");
        registeredEvents.add(regEvent[0]);
        registeredEvents.add(regEvent[1]);
        registeredEvents.add(regEvent[2]);
        registeredEvents.add(regEvent[3]);
        String allRegisteredEvents="";
        double cost = 0.0;
        for (int i=0;i<registeredEvents.size();i=i+4){
            allRegisteredEvents+="-"+registeredEvents.get(i);
            cost+=Double.parseDouble(registeredEvents.get(i+3));
        }
        jLabel73.setText(allRegisteredEvents);
        jLabel74.setText(String.valueOf("Total cost is "+cost+"$"));
    }//GEN-LAST:event_jButton33ActionPerformed

