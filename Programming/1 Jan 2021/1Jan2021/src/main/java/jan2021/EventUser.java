    private void jButton31ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton31ActionPerformed
        EventSearchFeesFrame.setVisible(false);
        EventFeesFoundFrame.setVisible(true);
        double fees = Double.parseDouble(jTextField10.getText());
        jTextField10.setText("");
        List<String> results = EventManagement.searchEventFees(fees);
        int a=0;
        for(int i=0; i<results.size();i=i+4){
                jTable3.setValueAt(results.get(i), a, 0);
                jTable3.setValueAt(results.get(i+1), a, 1);
                jTable3.setValueAt(results.get(i+2), a, 2);
                jTable3.setValueAt(results.get(i+3), a, 3);
                a++;
        }
    }//GEN-LAST:event_jButton31ActionPerformed

